<script>
(function () {
    // Generate a client-side idempotency key on page load
    const tsHex = Date.now().toString(16).toUpperCase();
    const randHex = Math.floor(Math.random() * 0xFFFFFF).toString(16).toUpperCase().padStart(6, '0');
    document.getElementById('idempotency_key').value = 'ik_' + tsHex + '_' + randHex;

    const variantOptions = @json($variantOptions);
    let rowIndex = 1;

    function buildOptions() {
        let html = '<option value="">Select variant</option>';
        for (const [id, label] of Object.entries(variantOptions)) {
            html += `<option value="${id}">${label}</option>`;
        }
        return html;
    }

    function refreshRemoveButtons() {
        const rows = document.querySelectorAll('.line-item-row');
        rows.forEach(row => {
            const btn = row.querySelector('.remove-item-btn');
            if (btn) btn.style.display = rows.length > 1 ? 'inline' : 'none';
        });
    }

    document.getElementById('add-item-btn').addEventListener('click', function () {
        const container = document.getElementById('line-items-container');
        const row = document.createElement('div');
        row.className = 'line-item-row flex items-end gap-3 bg-gray-50 border border-gray-200 rounded p-3';
        row.innerHTML = `
            <div class="flex-1 min-w-0">
                <label class="block mb-1 text-xs font-medium text-gray-600">Product Variant</label>
                <select name="items[${rowIndex}][product_variant_id]" required
                    class="block w-full px-3 py-2 border border-gray-300 rounded text-sm focus:outline-none focus:border-blue-500">
                    ${buildOptions()}
                </select>
            </div>
            <div class="w-32">
                <label class="block mb-1 text-xs font-medium text-gray-600">Quantity</label>
                <input type="number" name="items[${rowIndex}][quantity]" min="1" required
                    class="block w-full px-3 py-2 border border-gray-300 rounded text-sm focus:outline-none focus:border-blue-500"
                    placeholder="1">
            </div>
            <div class="flex-shrink-0 pb-0.5">
                <button type="button" class="remove-item-btn px-2 py-2 text-red-400 hover:text-red-600 text-lg leading-none" title="Remove">&times;</button>
            </div>
        `;
        container.appendChild(row);
        rowIndex++;
        refreshRemoveButtons();
    });

    document.getElementById('line-items-container').addEventListener('click', function (e) {
        if (e.target.closest('.remove-item-btn')) {
            e.target.closest('.line-item-row').remove();
            refreshRemoveButtons();
        }
    });
})();
</script>
